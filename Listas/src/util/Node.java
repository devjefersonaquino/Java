package util;

public class Node {
	
	private Double valor;
	private Node proximo;
	
	public Double getValor() {
		return valor;
	}
	
	public void setValor(Double valor) {
		this.valor = valor;
	}
	
	public Node getProximo() {
		return proximo;
	}
	
	public void setProximo(Node proximo) {
		this.proximo = proximo;
	}
	
	

}
